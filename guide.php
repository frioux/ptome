<strong>Searching</strong>
<p>To search for a book, go to the home page of the TOME system. From there
you can either search by general book information or by class.</p>
<strong>Class Information</strong>
<p>After selecting a class from the home page, you will be taken to a page with
information about that class. Each book for the class has the following
information:</p>
<ul>
   <li>
      General book information
   </li>
   <li>
      Whether the book has been marked as usable
   </li>
   <li>
      When the book's usable status was last verified
   </li>
   <li>
      Who changed the entry last
   </li>
   <li>
      Any comments about the verification (where the information about a
      book's usability came from should be listed here)
   </li>
   <li>
      How many books are available in the library
   </li>
</ul>
<p>When you find new information regarding the usability of books for classes,
please update this page with the latest information. This data will be shared
among all the TOMEs on campus, so with everyone working together, we
should be able to maintain a very accurate listing of what books are needed
for what classes. The only place to find official information from the
university about what books are needed for what classes is at the bookstore's
website: <a href="http://www.bkstr.com/CategoryDisplay/10001-9604-10236-1?demoKey=d">http://www.efollett.com/</a>.</p>
<p>This page also has an area for general comments about the class. Books can
be associated with a class by entering the book's ISBN number into the form
on the bottom of this page.</p>

<strong>Find Orphans</strong>
<p>Keeping books associated with classes is essential. If a book is allowed to
stay in the collection without any record of what class it is for, there's no way
of knowing if it goes out of date or becomes useless. This page will generate
a report showing books that are in any TOME but have not been associated
with a class. Every TOMEkeeper should work to keep this list empty at all
times.</p>

<strong>Find Useless</strong>
<p>Once all of the books have been associated with classes, this page will be
able to generate a report showing books that are no longer useful in any
classes. These books should be sold on Half.com as soon as possible, while
they still have value.</p>

