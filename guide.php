<p>Once a floor TOMEkeeper has been appointed, you still need several things
to get your floor's TOME up and running.</p>

<ul>
   <li>
      <p><strong>Storage space.</strong> We recommend a bookshelf of some sort in the
      TOMEkeeper's room. This has several advantages over a centrally
      located storage location:</p>
      <ul>
         <li>
            <p>The TOMEkeeper needs computer access (to use the database) to
            check books in and out.</p>
         </li>
         <li>
            <p>Having the books in the TOMEkeeper's room discourages people
            from just taking books (“It's only for this afternoon, I promise!”),
            forgetting to ever return them, and leaving you with no record of
            where they went.</p>
         </li>
         <li>
            <p>The TOMEkeeper's room can be locked to prevent theft.</p>
         </li>
      </ul>
      We recommend boxing the books and locking them the floor's storage
      closet during the summer.
   </li>
   <li>
      <p><strong>A stamp.</strong> The 41 TOME stamps the inside cover of all the books in the
      collection with a custom stamp from stamp-connection.com. It looks
      kind of like this:<br><br>

         <div class="center">
                                    41 TOME<br>
                                   Property of<br>
                                  ___________<br>
                                    Originator<br>
                                  ___________<br>
                                        ID<br>
                                  ___________<br>
         </div><br>
      Of course, you don't have to use this exact method, but it has
      worked well for us. One way or another, each book must be marked
      with its unique database ID number. This is the only way to identify and
      keep track of books.<br>
      The stamp clearly indicates which TOME the book belongs to,
      which is important because of the interTOME system.<br>
      Also, it tells you whether the book is a permanent donation. We put
      TOME under “Property of” if the book stays with us, and the donor's
      name if it does not.
   </li>
   <li>
      <p><strong>A team.</strong> The TOMEkeeper can't do it alone. Finding people to help
      out during the crunch times (beginning and end of the semester) and
      also to help manage other aspects of operation (such as selling the old
      books) is essential to success. Just let us know if we need
      to create user accounts for others on your floor.
   </li>
</ul>

