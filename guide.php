      <p>These are the policies that 41 has developed for its TOME. You're free
to change these as you see fit for your floor, but keep in mind that we've been
doing this for two years, and we know they work.<p>
<ul>
   <li>
      <p>Everyone on the floor is free to borrow from TOME, whether or not
      they have donated books into the system.</p>
   </li>
   <li>
      <p>However, abuse of the system (damaging books, not returning books,
      etc.) will result in a revocation of borrowing privileges.</p>
   </li>
   <li>
      <p>Only members of the floor are allowed to borrow from TOME. And
      no, it doesn't count to borrow a book for a friend.</p>
   </li>
   <li>
      <p>All first time borrowers need to sign a borrower agreement. See page
      13 for an example.</p>
   </li>
</ul>
<p>TOME relies on the honor system. We trust people by default.</p>

