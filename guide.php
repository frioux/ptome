<p>Log in with your username and password and you will arrive at the front page
of the TOME system.</p>
<strong>Semesters</strong><br>
<p>Many things in the database revolve around which semester a particular item
is associated with. The system as a whole has a default semester that can
always be seen in the upper right-hand corner of the screen. New entries of
any sort will always default to using this semester. If you wish to
temporarily override the system default semester, you can do so from the
          <a href="<?php print $path; ?>management.php">User and System Management</a> screen.
</p>
<strong>Adding a book to your floor's TOME</strong>
<ol>
   <li>
      Click “Add TOME Book” on the top menu bar
   </li>
   <li>
      Fill in the ISBN from the back of the book
   </li>
   <li>
      Put the donor's name in the Originator field
   </li>
   <li>
      If the donor wants the book back when they graduate, choose which
      semester the book should expire at the end of
   </li>
   <li>
      Add any special comments about the book such as “Front cover has
      water damage”
   </li>
   <li>
      The library should already point to your floor
   </li>
   <li>
      Click “Add TOME Book”
   </li>
   <li>
      You will be redirected to a page containing all the information the
      system has about the book you just added. The most important part of
      this right now is the newly generated book ID number. MARK THE
      BOOK WITH THE ID! And do it right away too, otherwise you'll
      forget and never find the book again.
   </li>
</ol>
<p>The TOME system will not let you add a book unless it knows about the
